    private void btnwidthdrawActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnwidthdrawActionPerformed
       
        int AccNum = Integer.valueOf(txtAccountnumber1.getText());
        double withdraw=Double.valueOf(txtwidthdraw.getText());
        Account ac = accDB.get(AccNum);
        double Balance;
        String NIC="";
        String bcNum="";
        int acNum;
        
        
        
        if (ac!=null){
            
          acNum = ac.getAccNum();
          if (ac.getBalance() >= withdraw){
          Balance = ac.getBalance() - withdraw;
         
          
          if(ac instanceof Major){
              
              Major mj=(Major)ac;
              NIC = mj.getNIC();
              ac = new Major(AccNum,Balance,NIC);
              
              
            }else{
              Minor mi=(Minor)ac;
              bcNum = mi.getBcNum();
              ac= new Minor(AccNum, Balance, bcNum);
                      
              
            
          }
        int result=accDB.update(ac);
        if  (result==1){
        
            txtwidthdraw.setText("");
            
          JOptionPane.showMessageDialog(rootPane, "Withdrawal Succeed");
          
          
        }else{
        
       
        JOptionPane.showMessageDialog(rootPane, "Withdrawal Failed");
        
        
        } 
        }else{
                
         txtwidthdraw.setText("");
        JOptionPane.showMessageDialog(rootPane, "Account not available !");        
          
                
                
          }
        }else{
            txtAccountnumber1.setText("");
        JOptionPane.showMessageDialog(rootPane, "Account not available !");
        }
    
    }//GEN-LAST:event_btnwidthdrawActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
        int AccNum= Integer.valueOf(JOptionPane.showInputDialog("enter account number"));
        Account ac=accDB.get(AccNum);
        if (ac!=null){
            
          txtaccount.setText(String.valueOf(ac.getAccNum()));
          txtbalance.setText(String.valueOf(ac.getBalance()));
          if(ac instanceof Major){
              
              Major mj=(Major)ac;
              rdomajor.setSelected(true);
              txtnic.setText(mj.getNIC());
              txtbc.setText("");
            }else{
              Minor mi=(Minor)ac;
              rdominor.setSelected(true);
              txtnic.setText("");
              txtbc.setText(mi.getBcNum());
          }
          
          
          
          
            
            
            
        }else{
            
            JOptionPane.showMessageDialog(rootPane, "Account not found");
            
        }
    }//GEN-LAST:event_jButton1ActionPerformed

