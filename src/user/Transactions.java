    private void btndepositActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btndepositActionPerformed
   
       
        int AccNum = Integer.valueOf(txtAccountnumber.getText());
        double deposit=Double.valueOf(txtdeposit.getText());
        Account ac = accDB.get(AccNum);
        double Balance;
        String NIC="";
        String bcNum="";
        int acNum;
        
        accDB.get(AccNum);
        
        if (ac!=null){
            
          acNum = ac.getAccNum();
          Balance = ac.getBalance() + Double.valueOf(txtdeposit.getText());
          if(ac instanceof Major){
              
              Major mj=(Major)ac;
              NIC = mj.getNIC();
              ac = new Major(AccNum,Balance,NIC);
              
              
            }else{
              Minor mi=(Minor)ac;
              bcNum = mi.getBcNum();
              ac= new Minor(AccNum, Balance, bcNum);
                      
              
            
          }
          int result=accDB.update(ac);
        if  (result==1){
        
            txtdeposit.setText("");
            
          JOptionPane.showMessageDialog(rootPane, "Deposit Succeed");
          
          
        }else {
        
            
        
            txtdeposit.setText("");
        
        JOptionPane.showMessageDialog(rootPane, "Deposit Failed");
        
        
        } }else{
                
         txtAccountnumber.setText("");
        JOptionPane.showMessageDialog(rootPane, "Account not available !");        
          
                
                
          }
        
                
                
                
           
   
        
    }//GEN-LAST:event_btndepositActionPerformed
    
